    private void btnRegistraLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistraLocalActionPerformed
       
        String nombre = txtNombre.getText();
        String cant  = txtVotantes.getText();
        
        nombre.trim();
        cant.trim();
        
        if(nombre.isEmpty()){
            error = true;
            message += "Nombre: Es invalido\n";
        }else
            local.setNombre(nombre);
        
        try{
        
            int votantes = Integer.parseInt(cant);
            if(votantes == 0){
                message += "Numero de votantes invalido";
                error = true;
            }else
                local.setCantidadVotantesRegistrados(votantes);
            
            
        }catch(Exception ex)
        {
        
           error = true;
           message += "Cantidad votantes no es numerico\n";
          
        
        };
        
        
        if (cbDistrito.getSelectedIndex() == 0){
        
            message += "No se selecciono distrito\n";
            error = true;
        }else
            local.setIdDistrito(((Distrito)cbDistrito.getSelectedItem()).getId());
        
        
        if(error){
             JOptionPane.showMessageDialog(null,message);
             return;
        }else{
        
                Manager.addLocal(local);
                JOptionPane.showMessageDialog(null,"Guardado satisfactoriamente");
                this.dispose();
        
        }
        
        
        
        
        
    }//GEN-LAST:event_btnRegistraLocalActionPerformed

