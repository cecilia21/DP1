    public void cargarDatos(){
    
        ArrayList<Region> listaRegiones = Manager.queryAllRegion();
        
        if(listaRegiones.isEmpty()){

            return;
        }
            
        for( Region r : listaRegiones){
        
            cbRegion.addItem(r);
        }   
        
        
        ItemListener il =  new ItemListener() {

            @Override
            public void itemStateChanged(ItemEvent e) {
         
                if(e.getStateChange() == ItemEvent.SELECTED){
                
                    ItemSelectable is = e.getItemSelectable();
                    Region dist = (Region) is.getSelectedObjects()[0];
                    
                     System.out.println(dist.getId());
                    System.out.println(dist.getCantidadVotantesRegistrados());
                    
                    ArrayList<Distrito> listaDistritos =    Manager.queryAllDistritoByRegion(dist.getId());
                    
                    
                    if(listaDistritos.isEmpty());
                        //do something
                    
                        
                     for(Distrito d : listaDistritos){
                     
                         cbDistrito.addItem(d);

                     }

                
                }
            
            
            }
        };

        
        cbRegion.addItemListener(il);
    
    
    }

