    public void listarResultados(ArrayList<PartidoPolitico> resultados){
        partidosModel = new MyTableModel();
        tabla_partidos.setModel(partidosModel);
        partidosModel.partidos = resultados;
        partidosModel.fireTableChanged(null);
    }
    class MyTableModel extends AbstractTableModel{
		ArrayList<PartidoPolitico> partidos = null; 
		String [] titles = {"Nombre"};
		@Override
		public int getColumnCount() {
			// TODO Auto-generated method stub
			return 1;
		}

		@Override
		public int getRowCount() {
			// TODO Auto-generated method stub
			return partidos.size();
		}

		@Override
		public Object getValueAt(int row, int col) {
			String value = "";
			switch(col){
				case 0:  value = "" + partidos.get(row).getNombre(); break;
				//case 1:  value = partidos.get(row).getName(); break;
				//case 2:  value = "" + productsLst.get(row).getPrice(); break;
				//case 3:  value = "" + productsLst.get(row).getStock(); break;				
			}
			return value;
		}
		
		public String getColumnName(int col){
			return titles[col];
		}
		
	}
